{
 printf("The ascii value of \\n is:%d\n",'\n');
 printf("The ascii value of \\r is:%d\n",'\r');
 printf("The ascii value of \\t is:%d\n",'\t');
 printf("The ascii value of \\a is:%d\n",'\a');
 printf("The ascii value of \\b is:%d\n",'\b');
 return 0;
}

